#include<bits/stdc++.h>
using namespace std;

int main () {

    // for
    for (int i = 1; i <= 10; i++) {
        cout << "Hemaan" << endl;
    }

    // while
    int i = 1;
    while (i <= 10) {
        cout << "hk" << endl;
        i++;
    }

    // do while- at least runs 1 time
    do {
        cout << "himanshu" << endl;
        i++;
    } while (i < 1);

    return 0;
}